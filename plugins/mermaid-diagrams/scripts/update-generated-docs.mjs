import { access, readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const pluginDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const referencesDir = path.join(pluginDir, 'skills/mermaid/references');
const mermaidSkillPath = path.join(pluginDir, 'skills/mermaid/SKILL.md');
const readmePath = path.join(pluginDir, 'README.md');
const thirdPartyNoticesPath = path.join(pluginDir, 'THIRD_PARTY_NOTICES.md');
const defaultDocsNavigationPath = path.join(
  pluginDir,
  'mermaid-source/packages/mermaid/src/docs/.vitepress/config.ts',
);

const generatedHeaderPattern =
  /^> \*\*Warning\*\*\n>\n> ## THIS IS AN AUTOGENERATED FILE\. DO NOT EDIT\.\n>\n> ## Please edit the corresponding file in \[[^\]]+\]\([^)]+\)\.\n\n/;
const syncStatusPattern =
  /<!-- BEGIN GENERATED SYNC STATUS -->\n([\s\S]*?)\n<!-- END GENERATED SYNC STATUS -->/;

const exists = async (filePath) => {
  try {
    await access(filePath);
    return true;
  } catch {
    return false;
  }
};

const titleFromId = (id) =>
  id
    .replace(/([a-z])([A-Z])/g, '$1 $2')
    .replace(/[-_]/g, ' ')
    .replace(/\b\w/g, (letter) => letter.toUpperCase());

const cleanText = (text) =>
  text
    .replace(/\[[^\]]*\]\([^)]*\)/g, (match) => match.match(/^\[([^\]]*)\]/)?.[1] ?? '')
    .replace(/`([^`]*)`/g, '$1')
    .replace(/\*\*([^*]*)\*\*/g, '$1')
    .replace(/\*([^*]*)\*/g, '$1')
    .replace(/<[^>]+>/g, '')
    .replace(/\s+/g, ' ')
    .trim();

const tableText = (text) => cleanText(text).replace(/\|/g, '\\|');

const cleanSidebarText = (text) =>
  cleanText(text)
    .replace(/[^\x20-\x7E]/g, '')
    .replace(/\s+/g, ' ')
    .trim();

const cleanTitle = (text) =>
  tableText(text)
    .replace(/\s*\(v\\?<[^)]*\)/g, '')
    .replace(/\s*\(v\\?\+\)/g, '')
    .trim();

const firstHeading = (text) => {
  const match = text.match(/^#\s+(.+)$/m);
  return match ? cleanTitle(match[1]) : null;
};

const firstUsefulParagraph = (text) => {
  const lines = text.split(/\r?\n/);
  const firstHeadingIndex = lines.findIndex((line) => /^#\s+/.test(line));
  const bodyLines = firstHeadingIndex === -1 ? lines : lines.slice(firstHeadingIndex + 1);
  const paragraph = [];
  let inFence = false;
  let skippingAdmonition = false;

  for (const rawLine of bodyLines) {
    const trimmed = rawLine.trim();

    if (/^```|^:::/.test(trimmed)) {
      inFence = !inFence;
      continue;
    }
    if (inFence) {
      continue;
    }
    if (!trimmed) {
      if (paragraph.length > 0) {
        break;
      }
      skippingAdmonition = false;
      continue;
    }
    if (/^#{1,6}\s+/.test(trimmed)) {
      continue;
    }
    if (/^>\s*\*\*(Warning|Note|Deprecated)\*\*/i.test(trimmed)) {
      skippingAdmonition = true;
      continue;
    }
    if (skippingAdmonition && trimmed.startsWith('>')) {
      continue;
    }
    if (!paragraph.length && (/^\|/.test(trimmed) || /^[-*]\s+/.test(trimmed) || /^!\[/.test(trimmed))) {
      continue;
    }

    paragraph.push(trimmed.replace(/^>\s?/, ''));
  }

  const summary = tableText(paragraph.join(' '));
  if (!summary) {
    return 'See reference documentation.';
  }

  const firstSentence = summary.match(/^.{1,220}?[.!?](?=\s|$)/)?.[0];
  return firstSentence ?? (summary.length > 220 ? `${summary.slice(0, 217).trim()}...` : summary);
};

const functionBlock = (text, functionName) => {
  const start = text.indexOf(`function ${functionName}()`);
  if (start === -1) {
    return '';
  }

  const openBrace = text.indexOf('{', start);
  if (openBrace === -1) {
    return '';
  }

  let depth = 0;
  for (let index = openBrace; index < text.length; index += 1) {
    if (text[index] === '{') {
      depth += 1;
    } else if (text[index] === '}') {
      depth -= 1;
      if (depth === 0) {
        return text.slice(openBrace + 1, index);
      }
    }
  }

  return '';
};

const loadNavigationMetadata = async () => {
  const configuredNavigation = Boolean(process.env.MERMAID_DOCS_NAVIGATION);
  const configuredPath = process.env.MERMAID_DOCS_NAVIGATION
    ? path.resolve(pluginDir, process.env.MERMAID_DOCS_NAVIGATION)
    : defaultDocsNavigationPath;

  if (!(await exists(configuredPath))) {
    if (configuredNavigation) {
      throw new Error(`Mermaid docs navigation file not found: ${configuredPath}`);
    }

    return new Map();
  }

  const config = await readFile(configuredPath, 'utf8');
  const syntaxBlock = functionBlock(config, 'sidebarSyntax');
  const metadata = new Map();
  const sectionPattern = /\{\s*text:\s*['"`]([^'"`]+)['"`][\s\S]*?items:\s*\[([\s\S]*?)\]\s*,?\s*\}/g;
  let sectionMatch;
  let order = 0;

  while ((sectionMatch = sectionPattern.exec(syntaxBlock))) {
    const category = cleanSidebarText(sectionMatch[1]) || 'Diagram Syntax';
    const items = sectionMatch[2];
    const itemPattern = /\{\s*text:\s*['"`]([^'"`]+)['"`]\s*,\s*link:\s*['"`]\/syntax\/([^'"`#]+)(?:#[^'"`]*)?['"`]\s*\}/g;
    let itemMatch;

    while ((itemMatch = itemPattern.exec(items))) {
      const title = cleanSidebarText(itemMatch[1]);
      const id = itemMatch[2];
      metadata.set(id, {
        category,
        order,
        title: cleanTitle(title || titleFromId(id)),
      });
      order += 1;
    }
  }

  return metadata;
};

export const extractDiagramMetadata = async (file, navigationMetadata = new Map()) => {
  const id = file.replace(/\.md$/, '');
  const text = await readFile(path.join(referencesDir, file), 'utf8');
  const nav = navigationMetadata.get(id);

  return {
    id,
    file,
    title: tableText(nav?.title ?? firstHeading(text) ?? titleFromId(id)),
    useCases: firstUsefulParagraph(text),
    category: tableText(nav?.category ?? 'Diagram Syntax'),
    order: nav?.order ?? Number.MAX_SAFE_INTEGER,
  };
};

const syntaxReferenceFiles = async (navigationMetadata = new Map()) => {
  const files = await readdir(referencesDir);
  return files
    .filter((file) => file.endsWith('.md'))
    .filter((file) => !file.startsWith('config-'))
    .filter((file) => file !== 'examples.md')
    .sort((left, right) => {
      const leftOrder = navigationMetadata.get(left.replace(/\.md$/, ''))?.order ?? Number.MAX_SAFE_INTEGER;
      const rightOrder =
        navigationMetadata.get(right.replace(/\.md$/, ''))?.order ?? Number.MAX_SAFE_INTEGER;
      return leftOrder - rightOrder || left.localeCompare(right);
    });
};

const stripGeneratedHeaders = async () => {
  const files = (await readdir(referencesDir)).filter((file) => file.endsWith('.md'));

  for (const file of files) {
    const filePath = path.join(referencesDir, file);
    const text = await readFile(filePath, 'utf8');
    const stripped = text.replace(generatedHeaderPattern, '');
    if (stripped !== text) {
      await writeFile(filePath, stripped);
    }
  }
};

const existingSyncStatus = async () => {
  if (!(await exists(readmePath))) {
    return null;
  }

  const readme = await readFile(readmePath, 'utf8');
  return readme.match(syncStatusPattern)?.[1].trim() ?? null;
};

const syncStatus = (fallbackStatus) => {
  if (
    process.env.MERMAID_SYNC_SOURCE ||
    process.env.MERMAID_SOURCE_COMMIT ||
    process.env.MERMAID_SYNC_DATE
  ) {
    const syncSource = process.env.MERMAID_SYNC_SOURCE ?? 'local references';
    const syncCommit = process.env.MERMAID_SOURCE_COMMIT ?? 'unknown';
    const syncDate = process.env.MERMAID_SYNC_DATE ?? new Date().toISOString();
    return `Last synced from Mermaid: ${syncSource} @ ${syncCommit} on ${syncDate}`;
  }

  return fallbackStatus ?? `Last synced from Mermaid: local references @ unknown on ${new Date().toISOString()}`;
};

const syncMetadata = (syncStatusLine) => {
  const match = syncStatusLine.match(/^Last synced from Mermaid: (.+) @ ([^\s]+) on (.+)$/);
  return {
    source: match?.[1] ?? 'local references',
    commit: match?.[2] ?? 'unknown',
    date: match?.[3] ?? 'unknown',
  };
};

const renderSkill = (diagrams) => {
  const table = [
    '| Type | Documentation | Use Cases |',
    '| ---- | ------------- | --------- |',
    ...diagrams.map(
      (diagram) =>
        `| ${diagram.title} | [${diagram.file}](references/${diagram.file}) | ${diagram.useCases} |`,
    ),
  ].join('\n');

  const diagramCount = diagrams.length;
  const extraCount = Math.max(diagramCount - 5, 0);

  return `---
name: mermaid
description: Generate Mermaid diagrams from user requirements. Supports ${diagramCount} synced Mermaid diagram types including flowcharts, sequence diagrams, class diagrams, ER diagrams, Gantt charts, and ${extraCount} more.
---

# Mermaid Diagram Generator

Generate high-quality Mermaid diagram code based on user requirements.

## Workflow

1. **Understand Requirements**: Analyze user description to determine the most suitable diagram type.
2. **Read Documentation**: Read the corresponding syntax reference for the diagram type.
3. **Generate Code**: Generate Mermaid code following the specification.
4. **Apply Styling**: Apply appropriate themes and style configurations.
5. **Optionally Lint**: If \`mmdc\` is installed, optionally render generated diagrams to validate syntax. If \`mmdc\` is unavailable and linting was not explicitly requested, skip linting by default.

## Diagram Type Reference

Select the appropriate diagram type and read the corresponding documentation. If a requested diagram type is not listed here, inspect \`references/\` before declaring it unsupported.

<!-- BEGIN GENERATED DIAGRAM TYPES -->
${table}
<!-- END GENERATED DIAGRAM TYPES -->

## Configuration & Themes

- [Theming](references/config-theming.md) - Custom colors and styles
- [Directives](references/config-directives.md) - Diagram-level configuration
- [Layouts](references/config-layouts.md) - Layout direction and spacing
- [Configuration](references/config-configuration.md) - Global settings
- [Math](references/config-math.md) - LaTeX math support

## Linting

If \`mmdc\` is installed, optionally render generated diagrams with the \`mermaid-lint\` skill or an equivalent \`mmdc\` command before declaring the diagram ready. If \`mmdc\` is not installed and the user did not explicitly ask for linting, skip this step and mention that linting was not run.

## Output Specification

Generated Mermaid code should:

1. Be wrapped in \`\`\`mermaid code blocks
2. Have correct syntax that renders directly
3. Have clear structure with proper line breaks and indentation
4. Use semantic node naming
5. Include styling when needed to improve visual appearance

## Example Output

\`\`\`mermaid
flowchart TD
    A[Start] --> B{Condition}
    B -->|Yes| C[Execute]
    B -->|No| D[End]
    C --> D
\`\`\`
`;
};

const renderReadme = (diagrams, syncStatusLine) => {
  const byCategory = new Map();
  for (const diagram of diagrams) {
    const list = byCategory.get(diagram.category) ?? [];
    list.push(`${diagram.title} (\`${diagram.file}\`)`);
    byCategory.set(diagram.category, list);
  }

  const supportedRows = [...byCategory.entries()]
    .map(([category, diagramsInCategory]) => `| ${category} | ${diagramsInCategory.join(', ')} |`)
    .join('\n');

  return `# mermaid-diagrams

Generate and validate Mermaid diagrams with synced Mermaid syntax references.

## Skills

| Skill | Description |
|---|---|
| \`mermaid\` | Generate Mermaid diagrams from user requirements, using local syntax references for supported diagram types. |
| \`mermaid-lint\` | Validate Mermaid code blocks by rendering them with \`mmdc\` and reporting lint status and errors. |

## Notes

- \`mermaid-lint\` requires the \`mmdc\` command from \`@mermaid-js/mermaid-cli\`.
- The synced references are bundled with the plugin so diagram generation can use local syntax documentation.

## Supported Diagrams

<!-- BEGIN GENERATED SUPPORTED DIAGRAMS -->
| Category | Diagram Types |
| -------- | ------------- |
${supportedRows}
<!-- END GENERATED SUPPORTED DIAGRAMS -->

## Documentation Sync

This plugin includes a GitHub Action that checks the official [mermaid-js/mermaid](https://github.com/mermaid-js/mermaid) repository weekly. It syncs syntax and selected config docs, removes upstream autogenerated warning banners from references, derives diagram metadata from Mermaid docs, and regenerates \`skills/mermaid/SKILL.md\` plus this README from the synced files.

<!-- BEGIN GENERATED SYNC STATUS -->
${syncStatusLine}
<!-- END GENERATED SYNC STATUS -->
`;
};

const renderThirdPartyNotices = (syncStatusLine) => {
  const metadata = syncMetadata(syncStatusLine);

  return `# Third-Party Notices

## Mermaid documentation

The Markdown files in \`skills/mermaid/references\` are synced from the
\`${metadata.source}\` repository.

- Source: https://github.com/mermaid-js/mermaid
- Synced commit: \`${metadata.commit}\`
- Synced date: ${metadata.date}
- License: MIT

The generated warning banners from the upstream documentation build are removed
from the bundled reference files so the skill can use the documentation directly.
`;
};

export const updateGeneratedDocs = async () => {
  const fallbackSyncStatus = await existingSyncStatus();
  await stripGeneratedHeaders();
  const navigationMetadata = await loadNavigationMetadata();
  const files = await syntaxReferenceFiles(navigationMetadata);
  const diagrams = await Promise.all(files.map((file) => extractDiagramMetadata(file, navigationMetadata)));
  const syncStatusLine = syncStatus(fallbackSyncStatus);
  await writeFile(mermaidSkillPath, renderSkill(diagrams));
  await writeFile(readmePath, renderReadme(diagrams, syncStatusLine));
  await writeFile(thirdPartyNoticesPath, renderThirdPartyNotices(syncStatusLine));
};

if (import.meta.url === `file://${process.argv[1]}`) {
  await updateGeneratedDocs();
}
